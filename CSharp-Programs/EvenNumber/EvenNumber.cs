
/// <summary>
///  Program description:
///  Write a program that receive a number and print on the console if the number is even number or not         
/// </summary>
/// 

public class EvenNumber
{
    public static bool Run(int number)
    {
        if (number % 2 == 0)
        {
            return true;
        } else
        {
            return false;
        }
    }
}

